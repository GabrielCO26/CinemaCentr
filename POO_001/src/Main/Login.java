    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        User = UserField.getText();
        Password = PasswordField.getText();

        if (User.equals("")) {
            Error1Text.setText("Ingrese un usuario");
            ConUser = false;
        } else {
            Error1Text.setText(null);
            ConUser = true;
        }

        if (Password.equals("")) {
            Error2Text.setText("Ingrese una contrase침a");
            ConPassword = false;
        } else {
            Error2Text.setText(null);
            ConPassword = true;
        }

        if (ConUser && ConPassword) {
            LoginSystem.loginUser(User, Password, this);

            if (loggedIn) {
                System.out.println("Usuario: " + loggedInUser.getUsername());
                System.out.println("Estado de usuario: " + loggedInUser.getState());
                this.dispose();
                if (loggedInUser.getState().equals("new")) {
                    CrearCine CrearCine = new CrearCine(loggedInUser);
                    CrearCine.setVisible(true);
                } else {
                    MainFrame MainFrame = new MainFrame(loggedInUser);
                    MainFrame.setVisible(true);
                }
            } else {
                Error1Text.setText("Usuario o contrase침a incorrectos");
            }
        }
    }//GEN-LAST:event_LoginButtonActionPerformed

