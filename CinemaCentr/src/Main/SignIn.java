    private void RegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterButtonActionPerformed
        User = UserField.getText();
        Password = PasswordField.getText();
        ConfPassword = ConfirmPasswordField.getText();

        if (Password.equals("")) {
            Error2Text.setText("Ingrese una contrase√±a");
            ConPassword = false;
        } else {
            if (!Password.equals(ConfPassword)) {
                Error2Text.setText("Las contrase√±as deben ser iguales");
                ConPassword = false;
            } else {
                Error2Text.setText(null);
                ConPassword = true;
            }
        }
        if (User.equals("")) {
            Error1Text.setText("Ingrese un usuario");
            ConUser = false;
        } else {
            Error1Text.setText(null);
            ConUser = true;
        }

        if (ConUser && ConPassword) {
            LoginSystem.createUser(User, Password, Error1Text, UserField, PasswordField, ConfirmPasswordField, new NewAccount(this,true));
        }
    }//GEN-LAST:event_RegisterButtonActionPerformed

