    public void getUserIndex() {
        int loggedInUserIndex = -1;
        for (int i = 0; i < userList.size(); i++) {
            if (userList.get(i).getUsername().equals(loggedInUser.getUsername())) {
                loggedInUserIndex = i;
                break;
            }
        }
        loggedInUser = userList.get(loggedInUserIndex);
    }

