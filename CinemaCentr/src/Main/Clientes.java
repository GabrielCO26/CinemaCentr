    private void RegistrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarButtonActionPerformed
        nombre = NameField.getText();
        correo = CorreoField.getText();
        telefono = TelefonoField.getText();
        id = IdField.getText();
        suscripcionIndex = SuscripcionesBox.getSelectedIndex();
        
        if (nombre.equals("")) {
            Error1Text.setText("Ingrese un nombre");
            ConNombre = false;
        } else {
            Error1Text.setText(null);
            ConNombre = true;
        }

        if (correo.equals("")) {
            Error2Text.setText("Ingrese un correo");
            ConCorreo = false;
        } else {
            Error2Text.setText(null);
            ConCorreo = true;
        }

        if (telefono.equals("")) {
            Error3Text.setText("Ingrese un telefono");
            ConTelefono = false;
        } else {
            Error3Text.setText(null);
            ConTelefono = true;
        }

        if (id.equals("")) {
            Error4Text.setText("Ingrese una identificación");
            ConId = false;
        } else {
            Error4Text.setText(null);
            ConId = true;
        }

        if (suscripcionIndex == -1) {
            Error5Text.setText("No hay suscripciones");
            ConSuscripcion = false;
        } else {
            Error5Text.setText(null);
            suscripcion = (String) SuscripcionesBox.getSelectedItem();
            System.out.println(suscripcion);
            ConSuscripcion = true;
        }

        if (ConNombre && ConCorreo && ConTelefono && ConId && ConSuscripcion) {
            clientesSystem.createCliente(username, nombre, correo, telefono, id, suscripcion, Error2Text, Error3Text, Error4Text, NameField, CorreoField, TelefonoField, IdField);
            updateTable();
            ClientesTable.setModel(clientesModel);
        }
    }//GEN-LAST:event_RegistrarButtonActionPerformed

    private void TelefonoFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TelefonoFieldKeyTyped
        int key = evt.getKeyChar();
        boolean numeros = key >= 48 && key <= 57;
        if (!numeros) {
            evt.consume();
        }
    }//GEN-LAST:event_TelefonoFieldKeyTyped

