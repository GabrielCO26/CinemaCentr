    public void updateTable() {
        clientesModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Desactivar la edición de todas las celdas
            }
        };

        clientesModel.addColumn("Nombre");
        clientesModel.addColumn("Correo");
        clientesModel.addColumn("Teléfono");
        clientesModel.addColumn("ID");
        clientesModel.addColumn("Suscripción");

        Collections.sort(clientesList, Comparator.comparing(Cliente::getSuscripcion));
        
        // Agregar los datos de los clientes al modelo de la tabla
        for (Cliente cliente : clientesList) {
            Object[] rowData = {cliente.getNombre(), cliente.getCorreo(), cliente.getTelefono(), cliente.getId(), cliente.getSuscripcion()};
            clientesModel.addRow(rowData);
        }
    }

    public void setComboModel() {
        // Recorrer el arreglo de suscripciones y obtener los nombres
        for (Suscripcion suscripcion : suscripcionesList) {
            String nombreSuscripcion = suscripcion.getNombre();
            comboBoxModel.addElement(nombreSuscripcion);
        }
    }

