    private void NameFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NameFieldKeyTyped
        int key = evt.getKeyChar();

        boolean mayusculas = key >= 65 && key <= 90;
        boolean minusculas = key >= 97 && key <= 122;
        boolean espacio = key == 32;

        if (!(minusculas || mayusculas || espacio)) {
            evt.consume();
        }
    }//GEN-LAST:event_NameFieldKeyTyped

    private void DeleteUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteUserActionPerformed
        // Obtener la fila seleccionada
        int selectedRow = ClientesTable.getSelectedRow();

        // Verificar si hay una fila seleccionada
        if (selectedRow != -1) {
            // Obtener el cliente correspondiente a la fila seleccionada
            Cliente clienteSeleccionado = clientesList.get(selectedRow);

            // Eliminar el cliente del arreglo y del archivo
            clientesSystem.removeCliente(clienteSeleccionado);
            clientesList.remove(clienteSeleccionado);

            updateTable();
            ClientesTable.setModel(clientesModel);
            System.out.println("Cliente eliminado");
        } 

    }//GEN-LAST:event_DeleteUserActionPerformed


